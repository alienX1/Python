# String Functions:
demo = "Aakash is a good boy"
print(demo.endswith("boy"))  # returns true when given sentence ends with boy
print(demo.count('o'))  # counts the given character
print(demo.capitalize())  # first letter capital
print(demo.upper())  # coverts every word to uppercase
print(demo.lower())  # coverts every word to lowercase
print(demo.find("is"))  # find the index of given word
print(demo.replace("is", "are"))  # used to replace one word with another
